public class AssigningSum {
    public static void main(String[] args) {
        int numCoins;
        int numNickels;
        int numDimes;

        numNickels = 5;
        numDimes = 6;

        numCoins = numNickels + numDimes;

        System.out.print("There are ");
        System.out.print(numCoins);
        System.out.println(" coins");
    }
}